<p align="center" style="color:#473A52;font-family:黑体;font-size:18px;font-weight:bold;">
	<span style="color:#000000;">校训：“厚德博学、和而不同”</span> 
</p>
<p align="center" style="color:#473A52;font-family:黑体;font-size:18px;font-weight:bold;">
	<strong><span style="color:#000000;">释义——</span></strong><span style="color:#000000;font-family:Simsun;font-size:14px;">&nbsp;</span> 
</p>
<p style="color:#473A52;font-family:Simsun;font-size:14px;">
	<span style="color:#000000;">&nbsp;&nbsp;&nbsp;&nbsp;“厚德”，出自《易经·坤卦》，“地势坤，君子以厚德载物”（地势舒展，君子取法于地，以深厚的德行来成就事业）。“厚德”即广厚之德，犹如“大德”；大地具有广厚之德，气势厚实和顺，故能载生万物。与此相对应，君子应增厚美德，容载万物。对于我校学生，“厚德”就是要在“爱国守法、明礼诚信、团结友善、勤俭自强、敬业奉献”的基础上，具有高尚的思想品德和良好的职业道德。&nbsp;</span><br>
<span style="color:#000000;"> &nbsp;&nbsp;&nbsp;&nbsp;“博学”，语出《礼记·中庸》，先贤们在《中庸》中告诫学子们要“博学之，审问之，慎思之，明辨之，笃行之”。其实这是为学的几个层次，或者说是几个递进的阶段。从“博学”到“笃行”，主要是用来勉励莘莘学子的。学子们要在具有广博的知识、较宽厚的专业知识的基础上，不断涉猎相关学科、相关领域的知识。&nbsp;</span><br>
<span style="color:#000000;"> &nbsp;&nbsp;&nbsp;&nbsp;“和而不同”，语出《论语·子路》：“君子和而不同，小人同而不和。” &nbsp;&nbsp;&nbsp;&nbsp;从哲学意义上讲，“和”是和谐，是统一，“同”是相同，是一致；“和”是抽象的，内在的；“同”是具体的，外在的。“和而不同”，是一个辩证的统一体，它的基础首先是有“不同”，有了“不同”，经过交流、沟通和协调，才可能达到内在的和谐统一。与此同时，只有在大目标不冲突的前提下，承认差异，包容差异，乃至尊重差异，才能化解矛盾，共存共荣，表现出广博的文化宽容与文化共享的情怀。从另一层面上来说，“和而不同”之意首先是讲和谐，讲团结，讲大局；其次，在“和”的基础上，每个人都可以有自己“不同”的思维，可以有独立的思考和思想，不人云亦云，墨守成规，只有这样才能有创新和创造，才能培养更高层次的创新型人才和大师。</span> 
</p>
<span style="color:#000000;line-height:24px;font-family:Simsun;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;校训“厚德博学、和而不同”，实事求是地从我校发展历史实际出发，综合了学校各校区的办学传统、文化底蕴、办学特色和教育目标，立足了学校的新任务、新目标、新形象，能激励和劝勉教师和学子们，同时也能体现我校的办学原则与目标，很好地体现了学校精神的核心和灵魂，较好的概括了学校的整体价值追求，反映了学校的独特气质和雍容高雅的名校风范。与此同时，校训大气、简练、通俗、易记。&nbsp;</span><br>
<br>
<p align="center" style="color:#473A52;font-family:黑体;font-size:18px;font-weight:bold;">
	<span style="color:#000000;">校风：“明德、精业、求实、创新”</span> 
</p>
<strong><span style="color:#000000;">&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;&nbsp; &nbsp;&nbsp;</span><strong><span style="color:#000000;">释义——</span></strong><span style="color:#000000;font-family:Simsun;font-size:14px;font-weight:bold;">&nbsp;</span></strong> <br>
<span style="color:#000000;line-height:24px;font-family:Simsun;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;“明德”意为彰明伦理，完善品德，具有理解、普及和恪守道德之义，包含与人为善、和谐相处的意义，合乎我校加强文化素质教育。语出《礼·君陈》“黍稷非馨，明德惟馨”，又见《礼·大学》“大学之道，在明明德”；意思是大学的根本宗旨，首先在于了解和彰显人性固有的内在道德光明，然后必须亲近人民并竭尽全力为之服务，最后则要通过个人修身与社会实践，使社会和人都臻于和谐圆满的至善至美的理想境界。&nbsp;</span><br>
<span style="color:#000000;line-height:24px;font-family:Simsun;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;“精业”，是“业精”转化而来。“业精”出自韩愈的《进学解》“业精于勤，荒于嬉;行成于思，毁于随”，可见，学习要在博的基础上求精，在精的基础上又不断的创新，使每个学生都有一技之长。业精是博厚的继承和发展，在博的基础上求精、求新，体现一种求实创新精神。而“精业”是一种动态的学习过程，一种发展的求索之境，是在业精基础之上有着更深远的哲学意义和指导意义。&nbsp;</span><br>
<span style="color:#000000;line-height:24px;font-family:Simsun;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;“求实”，就是讲求实际，一切从实际出发，因地、因时制宜，求真务实。它要求师生员工对工作、学习和生活要有认真、细致、严谨、扎实的态度和作风。以脚踏实地的态度来对待工作；以严谨科学的态度来对待学术；以诚心向学的态度来对待学业；以执着不懈的精神来追求真理。反对敷衍塞责、马虎应付的态度和轻率浮躁、疲塌懒散、急功近利的作风。不弄虚作假，不贪图虚名，不驰于空想，不鹜于虚声，唯以求真务实的态度做扎实的功夫，在实中求业绩、求学问、求精进，以形成良好的校风和校园文化。&nbsp;</span><br>
<span style="color:#000000;line-height:24px;font-family:Simsun;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;“创新” 意出《大学》引汤之《盘铭》“苟日新，日日新，又日新”。意为与时俱进，追求新高，与我校创建省内一流、国内知名大学的现代办学理念一致，同时又与上面的“求实”形成一脉相传的意蕴。正如江泽民所解释的：“科学精神的内涵很丰富，最基本的要求是求真务实，开拓创新。”“求实是科学之本，创新是科技发展的生命力所在。科学研究来不得半点虚假，必须以求实的态度，尊重客观规律，探索真理，开拓创新。”&nbsp;&nbsp;</span><br>
<p>
	<span style="color:#000000;line-height:24px;font-family:Simsun;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;校风作为学校办学宗旨的集中体现，也是学校教风与学风的浓缩。校风“明德、精业、求实、创新”反映了我校的办学方向、办学思想、办学精神、办学特色和人才培养要求，有利于推动学校统一办学精神的形成，促进办学特色氛围的形成，推动学校校园精神的形成。整体来看，校风精练、厚重，具有特色和个性。</span> 
</p>
